// Linked List Implementation
/*
 * Linked List: A dynamic data structure where elements are stored in nodes,
 * each containing data and a pointer to the next node.
 *
 * Time Complexity:
 * - Append: O(n) (traverses to end)
 * - Get: O(n) (traverses to index)
 * - Remove: O(n) (traverses to index)
 *
 * Space Complexity: O(n)
 *
 * Used in: Storing team players, allowing dynamic addition and access.
 */
template<typename T>
class Node {
public:
    T data;          // Data stored in the node
    Node* next;      // Pointer to the next node
    Node(T d) : data(d), next(nullptr) {}  // Constructor initializes data and sets next to null
};

template<typename T>
class LinkedList {
private:
    Node<T>* head;   // Pointer to the first node
    int size;        // Number of elements in the list
public:
    LinkedList() : head(nullptr), size(0) {}  // Constructor initializes empty list

    ~LinkedList() {  // Destructor to free memory
        Node<T>* current = head;
        while (current) {
            Node<T>* next = current->next;
            delete current;
            current = next;
        }
    }

    // Append a new element to the end of the list
    void append(T data) {
        Node<T>* newNode = new Node<T>(data);  // Create new node
        if (!head) {
            head = newNode;  // If list is empty, set head
        } else {
            Node<T>* temp = head;
            while (temp->next) temp = temp->next;  // Traverse to end
            temp->next = newNode;  // Append new node
        }
        size++;  // Increment size
    }

    // Get reference to element at index (for modification)
    T& get(int index) {
        if (index < 0 || index >= size) throw std::out_of_range("Index out of range");
        Node<T>* temp = head;
        for (int i = 0; i < index; i++) temp = temp->next;  // Traverse to index
        return temp->data;  // Return reference to data
    }

    // Get the size of the list
    int getSize() { return size; }

    // Remove element at index
    void remove(int index) {
        if (index < 0 || index >= size) return;
        if (index == 0) {
            Node<T>* temp = head;
            head = head->next;
            delete temp;
        } else {
            Node<T>* temp = head;
            for (int i = 0; i < index - 1; i++) temp = temp->next;
            Node<T>* toDelete = temp->next;
            temp->next = toDelete->next;
            delete toDelete;
        }
        size--;  // Decrement size
    }
};

