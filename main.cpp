// Heap (Max Heap) Implementation
/*
 * Heap: A specialized tree-based data structure that satisfies the heap property.
 * In a max heap, for any given node, the value of the node is greater than or equal
 * to the values of its children.
 *
 * Operations:
 * - Insert: Add a new element and maintain heap property
 * - Extract Max: Remove and return the maximum element
 * - Peek: Return the maximum element without removing
 *
 * Time Complexity: O(log n) for insert and extract
 * Space Complexity: O(n)
 *
 * Applications: Priority queues, heap sort, etc.
 */
class MaxHeap {
private:
    std::vector<int> heap;  // Vector to store heap elements

    // Helper function to maintain heap property after insertion
    void heapifyUp(int index) {
        while (index > 0) {
            int parent = (index - 1) / 2;
            if (heap[index] > heap[parent]) {
                std::swap(heap[index], heap[parent]);
                index = parent;
            } else {
                break;
            }
        }
    }

    // Helper function to maintain heap property after extraction
    void heapifyDown(int index) {
        int size = heap.size();
        while (true) {
            int left = 2 * index + 1;
            int right = 2 * index + 2;
            int largest = index;

            if (left < size && heap[left] > heap[largest]) largest = left;
            if (right < size && heap[right] > heap[largest]) largest = right;

            if (largest != index) {
                std::swap(heap[index], heap[largest]);
                index = largest;
            } else {
                break;
            }
        }
    }

public:
    // Insert a new element into the heap
    void insert(int value) {
        heap.push_back(value);  // Add to end
        heapifyUp(heap.size() - 1);  // Maintain heap property
    }

    // Extract and return the maximum element
    int extractMax() {
        if (heap.empty()) throw std::out_of_range("Heap is empty");
        int maxVal = heap[0];
        heap[0] = heap.back();  // Move last element to root
        heap.pop_back();  // Remove last element
        if (!heap.empty()) heapifyDown(0);  // Maintain heap property
        return maxVal;
    }

    // Return the maximum element without removing
    int peek() {
        if (heap.empty()) throw std::out_of_range("Heap is empty");
        return heap[0];
    }

    // Check if heap is empty
    bool isEmpty() { return heap.empty(); }

    // Get size of heap
    int size() { return heap.size(); }
};

